from marshmallow import Schema, fields, validate

class SocieteSchema(Schema):
    id = fields.Str(dump_only=True)
    symbole = fields.Str(required=True, validate=validate.Length(min=1, max=10))  # <-- was symbol
    date_maj = fields.Str(required=True)  # <-- was date
    price = fields.Float()
    marketCap = fields.Float()
    beta = fields.Float()
    lastDividend = fields.Float()
    range = fields.Str()
    change = fields.Float()
    changePercentage = fields.Float()
    volume = fields.Int()
    averageVolume = fields.Int()
    companyName = fields.Str()
    currency = fields.Str(validate=validate.Length(equal=3))
    cik = fields.Str()
    isin = fields.Str()
    cusip = fields.Str()
    exchangeFullName = fields.Str()
    exchange = fields.Str()
    industry = fields.Str()
    website = fields.Str()
    description = fields.Str()
    ceo = fields.Str()
    sector = fields.Str()
    country = fields.Str()
    fullTimeEmployees = fields.Str()
    phone = fields.Str()
    address = fields.Str()
    city = fields.Str()
    state = fields.Str()
    zip = fields.Str()
    image = fields.Str()
    ipoDate = fields.Str()
    defaultImage = fields.Bool()
    isEtf = fields.Bool()
    isActivelyTrading = fields.Bool()
    isAdr = fields.Bool()
    isFund = fields.Bool()